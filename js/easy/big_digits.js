fs.readFileSync(process.argv[2]).toString().split('\n').forEach(function (line) {
	if (line === "") return;

	for (var l=0; l<6; l++) {
		for (var i=0, len=line.length; i<len; i++) {
			var c = line.charAt(i);
			if (!/\d/.test(c)) continue;
			process.stdout.write( digits[c][l] );
		}
		process.stdout.write( "\n" );
	}
});